    //Method for searching contact by ID
    public void LoadContactID()
    {
        try {
            pst = (PreparedStatement) con.prepareStatement("select id from contacts");
            rs = pst.executeQuery();
            txtid.removeAllItems();
            while(rs.next())
            {
                txtid.addItem(rs.getString(1));
            }
            
          
        } catch (SQLException ex) {
            Logger.getLogger(PhoneBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    
    
    
    
