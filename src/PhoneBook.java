    // Update contact information by Update button
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
            try {
          
            String fname = txtfname.getText();
            String lname = txtlname.getText();
            String phoneNum = txtphoneNum.getText();
            String email = txtemail.getText();
            String address = txtaddress.getText();
            String id = txtid.getSelectedItem().toString();
            
            
            pst = (PreparedStatement) con.prepareStatement("update contacts set fname=?, lname=?, phoneNum=?, email=?, address=? where id=?");
            pst.setString(1, fname);
            pst.setString(2, lname);
            pst.setString(3, phoneNum);
            pst.setString(4, email);
            pst.setString(5, address);
            pst.setString(6, id);
            int k = pst.executeUpdate();
            
            
            
            if(k ==1)
            {
                JOptionPane.showMessageDialog(this, "Successfully updated a contact.");
                txtfname.setText("");
                txtlname.setText("");
                txtphoneNum.setText("");
                txtemail.setText("");
                txtaddress.setText("");
                txtfname.requestFocus();
                LoadContactID(); 
   
            }
            else
            {
                 JOptionPane.showMessageDialog(this, "Failed to update a contact.");
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(PhoneBook.class.getName()).log(Level.SEVERE, null, ex);
        }
          
    }//GEN-LAST:event_jButton1ActionPerformed

    
    //Display contact ID for search
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        String id = txtid.getSelectedItem().toString();
        
        try {
            pst = (PreparedStatement) con.prepareStatement("select * from contacts where id = ?");
            pst.setString(1, id);
            rs = pst.executeQuery();
            
            
            if(rs.next() == true)
            {
                txtfname.setText(rs.getString(2));
                txtlname.setText(rs.getString(3));
                txtphoneNum.setText(rs.getString(4));
                txtemail.setText(rs.getString(5));
                txtaddress.setText(rs.getString(6));
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(PhoneBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    
